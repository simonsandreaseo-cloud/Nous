export const MetadataField = ({ label, value }: { label: string, value: string }) => {
    const copy = () => {
        navigator.clipboard.writeText(value);
    };
    return (
        <div style={styles.metadataRow as any}>
            <span style={styles.metadataLabel}>{label}</span>
            <div style={styles.metadataValue as any}>
                <span style={{ overflow: 'hidden', textOverflow: 'ellipsis', whiteSpace: 'nowrap', marginRight: '10px' }}>{value || '-'}</span>
                <button onClick={copy} style={styles.copyIconBtn as any} title="Copiar"><IconCopy /></button>
            </div>
        </div>
    );
};
