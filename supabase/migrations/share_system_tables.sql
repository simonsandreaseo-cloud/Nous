-- Enable UUID extension if not already enabled
create extension if not exists "uuid-ossp";

-- 1. Ensure content_drafts table exists
create table if not exists content_drafts (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id),
  title text,
  html_content text,
  strategy_data jsonb default '{}'::jsonb,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 2. Ensure seo_reports table exists
create table if not exists seo_reports (
  id bigint generated by default as identity primary key,
  user_id uuid references auth.users(id),
  domain text,
  report_data jsonb default '{}'::jsonb,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- 3. Add sharing columns to content_drafts
do $$
begin
  if not exists (select 1 from information_schema.columns where table_name = 'content_drafts' and column_name = 'share_token') then
    alter table content_drafts add column share_token uuid default uuid_generate_v4();
  end if;
  if not exists (select 1 from information_schema.columns where table_name = 'content_drafts' and column_name = 'public_access_level') then
    alter table content_drafts add column public_access_level text check (public_access_level in ('none', 'view', 'edit')) default 'none';
  end if;
end $$;

