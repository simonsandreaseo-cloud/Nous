-- 7. RLS Policies for sharing

-- content_drafts
alter table content_drafts enable row level security;

-- Policy: Users can view own drafts
drop policy if exists "Users can view own drafts" on content_drafts;
create policy "Users can view own drafts" on content_drafts
  for select using (auth.uid() = user_id);

-- Policy: Users can update own drafts
drop policy if exists "Users can update own drafts" on content_drafts;
create policy "Users can update own drafts" on content_drafts
  for update using (auth.uid() = user_id);

-- Policy: Users can delete own drafts
drop policy if exists "Users can delete own drafts" on content_drafts;
create policy "Users can delete own drafts" on content_drafts
  for delete using (auth.uid() = user_id);

-- Policy: Users can insert own drafts
drop policy if exists "Users can insert own drafts" on content_drafts;
create policy "Users can insert own drafts" on content_drafts
  for insert with check (auth.uid() = user_id);

-- Policy: Public access (View)
drop policy if exists "Public view shared drafts" on content_drafts;
create policy "Public view shared drafts" on content_drafts
  for select using (public_access_level = 'view' or public_access_level = 'edit');

-- Policy: Public access (Edit)
drop policy if exists "Public edit shared drafts" on content_drafts;
create policy "Public edit shared drafts" on content_drafts
  for update using (public_access_level = 'edit');


